#include <iostream>

using namespace std;

int main(){
    int num;

    cout<<"Digite numero: ";
    cin>>num;

    if(num == 1) cout<<"um";
    else if(num == 2) cout<<"dois";
    else if(num == 3) cout<<"tres";
    else if(num == 4) cout<<"quatro";
    else if(num == 5) cout<<"cinco";
    else if(num == 6) cout<<"seis";
    else if(num == 7) cout<<"sete";
    else if(num == 8) cout<<"oito";
    else if(num == 9) cout<<"nove";
    else cout<<"Numero invalido";
}



